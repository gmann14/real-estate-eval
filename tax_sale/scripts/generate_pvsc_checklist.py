"""Generate a copy-pasteable PVSC lookup checklist as Markdown.

Reads the unified dataset and emits a year-by-year table of every lot
that needs PVSC enrichment. Each row has the AAN (the PVSC search key),
the PID (if known, for cross-reference), the civic address (so you can
verify you're on the right record), and the opening bid (context).

Usage:
    python3 tax_sale/scripts/generate_pvsc_checklist.py
    python3 tax_sale/scripts/generate_pvsc_checklist.py --out FILE

The output is meant to be embedded into MANUAL-DATA-TODO.md (run this
script to regenerate when new years are added) or used standalone.
"""
from __future__ import annotations

import argparse
import sys
from collections import defaultdict
from pathlib import Path
from typing import Optional

REPO_ROOT = Path(__file__).resolve().parents[2]
# Make `tax_sale` importable regardless of cwd
if str(REPO_ROOT) not in sys.path:
    sys.path.insert(0, str(REPO_ROOT))


def _safe_money(v) -> str:
    return f"${v:,.0f}" if v is not None else "—"


def _ascii_address(s) -> str:
    return s.replace("|", "/") if s else "—"


def render_checklist(records: list[dict]) -> str:
    """Produce a year-grouped markdown checklist."""
    # Group by year. We want all "real bidding observations" — sold + no_bids
    # — because those are the records that feed comp scoring and field
    # strength. Redeemed lots are skipped (never reached bidding) and
    # withdrawn lots too (incomplete observations).
    by_year: dict[int, list[dict]] = defaultdict(list)
    for r in records:
        if r["outcome"] not in {"sold", "no_bids"}:
            continue
        if r.get("has_enrichment"):
            continue  # already done
        by_year[r["year"]].append(r)

    if not by_year:
        return "All lots already enriched. Nothing to look up.\n"

    out = []
    total = sum(len(v) for v in by_year.values())
    out.append(f"# PVSC Historical Lookup Checklist")
    out.append("")
    out.append(
        f"**{total} lots** need PVSC assessed-value lookup, "
        f"across {len(by_year)} years. "
        f"Generated by `tax_sale/scripts/generate_pvsc_checklist.py` from the "
        f"current dataset — re-run to refresh."
    )
    out.append("")
    out.append(
        "**Workflow** (full detail in [PVSC-LOOKUP-GUIDE.md](PVSC-LOOKUP-GUIDE.md)):"
    )
    out.append("")
    out.append(
        "1. Open <https://www.pvsc.ca/find-assessment> → click **Search by Assessment Account Number**."
    )
    out.append(
        "2. For each row below: paste the **AAN** into the search, solve the reCAPTCHA, "
        "read off assessed value, year built, lot acres, structure sqft from the result page."
    )
    out.append(
        "3. Open the per-year CSV (generated below) and paste the numbers into the right row."
    )
    out.append(
        "4. Cross-check the address PVSC shows against the **Civic Address** column — "
        "if they disagree by more than a house number, you may have hit a retired/renumbered AAN."
    )
    out.append("")
    out.append(
        "**Generate the per-year CSVs first** (these have the right column structure):"
    )
    out.append("")
    out.append("```bash")
    for year in sorted(by_year):
        out.append(
            f"python3 -m tax_sale enrichment-template --year {year} "
            f"--out data/enrichment/pvsc-{year}.csv"
        )
    out.append("```")
    out.append("")

    # Priority order: most recent year first (freshest assessments + freshest comps)
    for year in sorted(by_year.keys(), reverse=True):
        lots = by_year[year]
        # Sort sold first (more important), then by lot number
        lots.sort(key=lambda r: (r["outcome"] != "sold", r["lot_number"]))
        out.append(f"## {year} ({len(lots)} lots)")
        out.append("")
        out.append(
            f"_Estimated time: {len(lots) * 1.5:.0f}-{len(lots) * 2:.0f} min "
            f"(~1.5 min/lot for typical lookups; longer for multi-parcel or AAN-not-found cases)._"
        )
        out.append("")
        # Show the table
        out.append(
            "| ☐ | Lot # | AAN | PID | Civic Address | Community | Opening Bid | Outcome | Description |"
        )
        out.append(
            "|---|------:|-----|-----|---------------|-----------|------------:|---------|-------------|"
        )
        for r in lots:
            address = _ascii_address(r.get("display_address"))
            community = r.get("community") or "—"
            description = r.get("lot_description") or "—"
            out.append(
                f"| ☐ | {r['lot_number']} | `{r.get('aan') or '?'}` "
                f"| {f'`{r['pid']}`' if r.get('pid') else '—'} "
                f"| {address} | {community} | {_safe_money(r.get('opening_bid'))} "
                f"| {r['outcome']} | {description} |"
            )
        out.append("")

    out.append("---")
    out.append("")
    out.append("**After completing a year's lookups**, run:")
    out.append("")
    out.append("```bash")
    out.append("python3 -m tax_sale stats   # `has_enrichment` count should reflect the lots you filled in")
    out.append("```")
    out.append("")
    out.append(
        "**To regenerate this checklist after adding enrichment data:**"
    )
    out.append("")
    out.append("```bash")
    out.append(
        "python3 tax_sale/scripts/generate_pvsc_checklist.py "
        "--out tax_sale/PVSC-CHECKLIST.md"
    )
    out.append("```")
    out.append("")
    out.append(
        "Already-enriched lots are filtered out, so the doc shrinks as you make progress."
    )
    return "\n".join(out) + "\n"


def main(argv: Optional[list[str]] = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__.splitlines()[0])
    parser.add_argument(
        "--out", type=Path, default=None,
        help="Path to write the markdown checklist. Defaults to stdout.",
    )
    args = parser.parse_args(argv)

    # Lazy import so test discovery doesn't pay the load cost
    from tax_sale.dataset import load_all_lots
    records = load_all_lots()

    md = render_checklist(records)
    if args.out is None:
        print(md)
    else:
        args.out.parent.mkdir(parents=True, exist_ok=True)
        args.out.write_text(md)
        total_lots = md.count("\n| ☐ |")
        print(f"Wrote checklist to {args.out} ({total_lots} lots).", file=sys.stderr)
    return 0


if __name__ == "__main__":
    sys.exit(main())
